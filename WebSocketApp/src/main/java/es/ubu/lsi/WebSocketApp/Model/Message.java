package es.ubu.lsi.WebSocketApp.Model;

public class Message {
	
	private String form;
	private String text;
	
	public String getForm() {
		return form;
	}
	public void setForm(String form) {
		this.form = form;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	

}
